<script setup lang="ts">

/**
 * Log in to a user's Ridehub account
 */

import { ref, onBeforeMount} from 'vue'
// @ts-ignore
import { nameRules,pwRules} from '../../utils/rules'
import { myFetch } from '../../utils/fetch'
import { apiMethods} from '../../../../membership-server/src/common/apimethods'
import { User, Roles } from '../../../../membership-server/src/common/user'
import { Alert, AlertError } from '../../utils/alert'
import { mdiEye } from '@mdi/js'
import { mdiEyeOff } from '@mdi/js'

const loginForm = ref();
const userOrEmail = ref('');
const password = ref('');
const showPass = ref(false);
const remember = ref(false);

const emit = defineEmits(['loggedIn','forgotPass']);
const props = defineProps<{
  useremail : string
}>()

onBeforeMount( () => {
   if (window.localStorage.username !== undefined && window.localStorage.password !== undefined) {
    if (window.localStorage.username !== 'undefined' && window.localStorage.password !== 'undefined') {
            userOrEmail.value = window.localStorage.username;
            password.value = window.localStorage.password;
            remember.value = true;
        }
      }
      if (props.useremail.length > 0)
        userOrEmail.value = props.useremail;

  })

async function submit() {
  const {valid} = await loginForm.value?.validate()
  if (valid) {
    const username = userOrEmail.value;
    const pass = password.value;
    if (remember.value) {
        window.localStorage.username = username;
        window.localStorage.password = pass;
      }
    let user = new User(username,pass);
        
    myFetch(apiMethods.login,user)
      .then((response) => {
        
          const user : User = response;
          if (user != null) {
            
            if (user.id > 0) {
                if (user.role === Roles.None) {
                  Alert('Registration','You need to reply to your email to complete registration','','info','OK')
                  return;
                }
                console.log('user: ' + user.name);
                emit('loggedIn',user);
                return;

            } else {
              AlertError('Login unsuccessful','Username or password incorrect')
              emit('loggedIn',null);
              return;
            }
          }
          AlertError('Login unsuccessful','Could not contact server');
          emit('loggedIn',null);
      })
  }
}



</script>

<template>
  <div class="d-flex align-center flex-column">
  <v-card >
    <v-card-title class="headline black" primary-title>
      Sign in to TCC membership database
    </v-card-title>
    <v-card-text> Same username and password as used for RideHub</v-card-text>
    <v-card-text> If you have forgotten your password, please use RideHub method to retrieve it</v-card-text>
    <v-card-text class="pa-2">
      <v-form @submit.prevent="submit" ref="loginForm">
        <v-text-field v-model="userOrEmail"  :rules="nameRules" autocapitalize="off"
         label="Email or username" hint="Same email as your TCC email address">
        </v-text-field>
        <v-text-field v-model="password" :append-inner-icon="showPass ? mdiEye : mdiEyeOff"
          @click:append-inner="showPass = !showPass"   :type="showPass ? 'text' : 'password'" 
          hint="Same password as your TCC email account"
          :rules="pwRules"  label="Password">
        </v-text-field>

        <v-checkbox v-if="remember===false" 
          v-model="remember"
          label="Remember me on this browser"
          value="false"
        ></v-checkbox>
        <v-btn color="green" type="submit" block class="mt-2">    Sign in     </v-btn>
     
      </v-form>
    </v-card-text>
  </v-card>
  </div>

</template>

